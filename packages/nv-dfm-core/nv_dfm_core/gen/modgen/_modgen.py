class ModGen:
    """Module Generator that creates executable Python code from NetIR.

    ModGen takes the intermediate representation (NetIR) for a site and generates
    Python code implementing the Petri net execution logic.
    """

    def _generate_python_code(
        self, this_site_runtime_module: ModuleType, netir: NetIR
    ) -> str:
        """Generate Python code from the Net IR."""

        lines = [
            copyright,
            "from typing import Any",
            "from dataclasses import dataclass, field",
            "",
            "from nv_dfm_core.api import ErrorToken, StopToken",
            "from nv_dfm_core.exec import (",
            "    Activation,",
            "    activate_when_places_ready,",
            "    ControlPlace,",
            "    CountingPlace,",
            "    DfmContext,",
            "    Frame,",
            "    FlowInfo,",
            "    Net,",
            "    PanicError,",
            "    Place,",
            "    QueuePlace,",
            "    tagged_json_value_to_object,",
            "    TransitionTryActivateFunc,",
            ")",
            f"from {this_site_runtime_module.__name__} import ThisSite",
            "",
        ]
        ctx = GenContext(lines=lines)
        netir.emit_python(ctx)
        return "\n".join(lines)

    def generate_net_module(
        self,
        federation_name: str,
        this_site_name: str,
        this_site_runtime_module: ModuleType,
        netir: NetIR,
        logger: Logger,
        force_modgen: bool = False,
    ) -> ModuleType:
        """ModGen usually runs remotely to the app on each participating site of the federation.
        It takes the PrepraedPipeline that was earlier produces by IRGen (maybe on the app,
        maybe in the federation) and generates a Python module that contains
        the places and activations and transitions of the petri net."""

        # we want to cache already generated modules. We cache on two levels:
        # 1. when a module has been loaded into this instance of the python interpreter, use that.
        # 2. If a file with this netir's Python code has been generated, load that.
        # 3. If not, generate the python code in the cache and load it.

        # First, generate a name for this module. We add the current dfm code version to
        # make sure the cache is invalidated if anything changes. The other part of the name
        # is the fingerprint computed by IRGen on the client.
        sanitized_version = _sanitize_version_for_identifier(nv_dfm_core.__version__)
        module_name = f"dfm_v{sanitized_version}_netir_{netir.fingerprint}"

        assert module_name.isidentifier(), (
            f"Module name {module_name} is not a valid Python identifier"
        )

        # check if the module has been loaded in this instance already
        if module_name in sys.modules and not force_modgen:
            logger.info(f"ModGen returning already loaded net module {module_name}")
            return sys.modules[module_name]

        # check if the file with the python code exists already
        base_path = appdirs.user_cache_dir("dfm")  # pyright: ignore[reportUnknownMemberType, reportUnknownVariableType]
        assert isinstance(base_path, str)

        file_path = (
            Path(base_path)
            / "modgen-cache"
            / sanitized_version
            / federation_name
            / this_site_name
            / f"{module_name}.py"
        )
        if (
            file_path.exists()
            and not force_modgen
            and not nv_dfm_core.__version__ == "unknown"
        ):
            # we have a cached version, we don't want to force regeneration, and the version
            # is not unknown, so the file is good.
            logger.info(
                f"ModGen loading net module from cached python file: {file_path}"
            )
        else:
            # We don't have a cached file yet, or we want to regenerate it,
            # or the version is unknown (so we don't want to use the cached file),
            # let's generate the file.
            if nv_dfm_core.__version__ == "unknown":
                logger.warning("DFM version is unknown, forcing regeneration")
            # make sure the parent directories exist
            file_path.parent.mkdir(parents=True, exist_ok=True)
            logger.info(f"ModGen generating net module python file: {file_path}")
            # no, generate and save the file
            generated = self._generate_python_code(this_site_runtime_module, netir)
            debug = False
            if debug:
                print("+" * 120)
                print(generated)
                print("+" * 120)
            _ = file_path.write_text(generated, encoding="utf-8")

        # now the file should exist, load it
        return _load_script_file_as_module(module_name, file_path, logger)

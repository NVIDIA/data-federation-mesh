def _sanitize_version_for_identifier(version: str) -> str:
    """Convert version string to valid Python identifier component.

    Extracts base version and short commit hash, removing .devN and date parts.

    Examples:
        "3.0.10" -> "3_0_10"
        "3.0.10+gabc12345" -> "3_0_10_gabc12345"
        "3.0.11.dev1+gdc558fc93.d20260203" -> "3_0_10_gdc558fc9"
    """
    # Extract base version (before .dev or .post)
    base_match = re.match(r"^(\d+\.\d+\.\d+)", version)
    base_version = base_match.group(1) if base_match else version.split(".")[0]

    # Extract short commit hash (g followed by 8 hex chars)
    hash_match = re.search(r"\+g([0-9a-f]{8})", version)
    short_hash = f"_g{hash_match.group(1)}" if hash_match else ""

    # Build clean version: base + short hash
    clean_version = base_version.replace(".", "_") + short_hash

    # Final cleanup - remove any remaining invalid characters
    sanitized = re.sub(r"[^a-zA-Z0-9_]", "", clean_version)
    return sanitized


def _load_script_file_as_module(
    module_name: str, script: str | Path, logger: Logger
) -> ModuleType:
    # Python 3 docs:
    # https://docs.python.org/3/library/importlib.html#importing-a-source-file-directly
    if not isinstance(script, Path):
        script = Path(script)
    spec = spec_from_file_location(script.stem, script)
    logger.info(f"ModGen spec: {spec}")
    assert spec
    mod = module_from_spec(spec)
    logger.info(f"ModGen mod: {mod}")
    # add the module to the sys modules so we can reuse it if it exists
    # Note: the generated module is "stateless". All the state (i.e. the places)
    # will be instantiated freshly in NetRunner.collect_places(), which calls the
    # create_places() method generated into each module.
    sys.modules[module_name] = mod
    assert spec.loader
    spec.loader.exec_module(mod)
    return mod


